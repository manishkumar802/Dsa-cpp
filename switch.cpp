#include<iostream>
using namespace std;
int main(){
 int day;
 cin>>day;
 switch(day){
    case 1:
    cout<<"monday"<<endl;
    break;
  case 2:
    cout<<"tuesday"<<endl;
    break;

  case 3:
    cout<<"wednesday"<<endl;
    break;
  case 4:
    cout<<"thrusday"<<endl;
    break;

default:
cout<<"default case"<<endl;



 }

}
